package main

import (
	"bytes"
	"flag"
	"fmt"
	"go/format"
	"os"
	"path/filepath"
	"text/template"

	cfschema "github.com/hashicorp/aws-cloudformation-resource-schema-sdk-go"
	"github.com/mitchellh/cli"

	//generator "github.com/hashicorp/terraform-provider-aws-cloudapi/internal/service/cloudformation/schema-generator"
	"github.com/iancoleman/strcase"
)

var (
	resourceType     = flag.String("resource", "", "Terraform resource type; required")
	cfTypeSchemaFile = flag.String("cfschema", "", "CloudFormation resource type schema file; required")
	outputDirectory  = flag.String("output", ".", "output directory")
)

func usage() {
	fmt.Fprintf(os.Stderr, "Usage:\n")
	fmt.Fprintf(os.Stderr, "\tmain.go [flags] -resource <resource-type> -cfschema <CF-type-schema-file>\n\n")
	fmt.Fprintf(os.Stderr, "Flags:\n")
	flag.PrintDefaults()
}

func main() {
	flag.Usage = usage
	flag.Parse()

	if *resourceType == "" || *cfTypeSchemaFile == "" {
		flag.Usage()
		os.Exit(2)
	}

	filename := filepath.Join(*outputDirectory, *resourceType+"_schema_gen.go")

	ui := &cli.BasicUi{
		Reader:      os.Stdin,
		Writer:      os.Stdout,
		ErrorWriter: os.Stderr,
	}

	generator := newGenerator(ui, *resourceType, *cfTypeSchemaFile)

	if err := generator.generate(filename); err != nil {
		ui.Error(fmt.Sprintf("error generating Terraform resource schema: %s", err))
		os.Exit(1)
	}
}

type generator struct {
	cfTypeSchemaFile string
	tfResourceType   string
	ui               cli.Ui
}

func newGenerator(ui cli.Ui, tfResourceType, cfTypeSchemaFile string) *generator {
	return &generator{
		cfTypeSchemaFile: cfTypeSchemaFile,
		tfResourceType:   tfResourceType,
		ui: &cli.BasicUi{
			Reader:      os.Stdin,
			Writer:      os.Stdout,
			ErrorWriter: os.Stderr,
		},
	}
}

func (g *generator) infof(format string, a ...interface{}) {
	g.ui.Info(fmt.Sprintf(format, a...))
}

// generate generates the resource's schema definition in the specified file.
func (g *generator) generate(filename string) error {
	g.infof("generating Terraform schema for %q from %q into %q", g.tfResourceType, g.cfTypeSchemaFile, filename)

	resource, err := NewResourcePath(g.tfResourceType, g.cfTypeSchemaFile)

	if err != nil {
		return fmt.Errorf("error reading CloudFormation resource schema for %s: %w", *resourceType, err)
	}

	templateData := TemplateData{
		FunctionName: resource.FunctionNamePrefix + "Schema",
	}

	tmpl, err := template.New("function").Parse(templateBody)

	if err != nil {
		return fmt.Errorf("error parsing function template: %w", err)
	}

	var buffer bytes.Buffer
	err = tmpl.Execute(&buffer, templateData)

	if err != nil {
		return fmt.Errorf("error executing template: %w", err)
	}

	generatedFileContents, err := format.Source(buffer.Bytes())

	if err != nil {
		return fmt.Errorf("error formatting generated file: %w", err)
	}

	f, err := os.Create(filename)

	if err != nil {
		return fmt.Errorf("error creating file (%s): %w", filename, err)
	}

	defer f.Close()

	_, err = f.Write(generatedFileContents)

	if err != nil {
		return fmt.Errorf("error writing to file (%s): %w", filename, err)
	}

	// for propertyName := range resource.CfResource.Properties {
	// 	log.Printf("%s\n\n%s\n\n", propertyName, generator.RootPropertySchema(resource.CfResource, propertyName))
	// }

	return nil
}

var templateBody = `
// Code generated by generators/schema/main.go; DO NOT EDIT.

package provider

import (
	"github.com/hashicorp/terraform-plugin-sdk/v2/helper/schema"
)

// {{ .FunctionName }} returns a resource's Terraform schema.
func {{ .FunctionName }}() map[string]*schema.Schema {
	schema := map[string]*schema.Schema{}

	return schema
}
`

type TemplateData struct {
	FunctionName string
}

type Resource struct {
	CfResource         *cfschema.Resource
	FunctionNamePrefix string
	TfType             string
}

func NewResourcePath(resourceType, cfTypeSchemaFile string) (*Resource, error) {
	resourceSchema, err := cfschema.NewResourceJsonSchemaPath(cfTypeSchemaFile)

	if err != nil {
		return nil, fmt.Errorf("error reading CloudFormation Resource Type Schema: %w", err)
	}

	resource, err := resourceSchema.Resource()

	if err != nil {
		return nil, fmt.Errorf("error parsing CloudFormation Resource Type Schema: %w", err)
	}

	if err := resource.Expand(); err != nil {
		return nil, fmt.Errorf("error expanding JSON Pointer references: %w", err)
	}

	return &Resource{
		CfResource:         resource,
		FunctionNamePrefix: "resource" + strcase.ToCamel(resourceType),
		TfType:             resourceType,
	}, nil
}
